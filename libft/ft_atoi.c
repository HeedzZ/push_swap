#include "libft.h"

int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	ret;

	ret = 0;
	neg = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (ft_isdigit(str[i]) == 1)
	{
		ret = ret * 10 + str[i] - '0';
		i++;
	}
	return (ret * neg);
}

